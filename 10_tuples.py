# Trabalhando com Tuplas

# Definindo uma tupla
# Uma tupla é uma coleção ordenada e imutável de elementos
coordinates = (4, 5)

# Definindo uma lista de tuplas
coordinates_list = [(4, 5), (6, 7), (8, 9)]

# Acessando elementos de uma tupla
# A tupla é indexada, assim como as listas, onde o índice começa em 0
x_coordinate = coordinates[0]
y_coordinate = coordinates[1]

print("Coordenada X:", x_coordinate)  # Saída: Coordenada X: 4
print("Coordenada Y:", y_coordinate)  # Saída: Coordenada Y: 5]