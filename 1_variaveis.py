# Função print() - Usada para exibir informações no console

# Variáveis
# A sintaxe é: nome_da_variavel = valor_da_variavel
# O sinal de "=" é usado para atribuir um valor à variável

name = "Your Name" # Variável do tipo string (texto)
age = 35 # Variável do tipo numérico (inteiro)
height = 1.9 # Variável do tipo numérico (decimal)
is_male = False # Variável do tipo booleano (True | False)

# Utilizando a função print() para exibir informações
# Concatenando variáveis e strings
# O operador "+" é usado para concatenar (juntar) strings
print("My name is: " + name)

# É possível utilizar f-strings para uma concatenação mais elegante e eficiente
# f-strings permitem incorporar variáveis diretamente dentro das strings
print(f"My name is: {name} and I am {age} years old")
